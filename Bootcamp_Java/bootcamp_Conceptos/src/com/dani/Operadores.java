package com.dani;

public class Operadores {
    public static void main(String[] args) {
        // Tipos de operadores
        //Aritmeticos +, -, /, %, *
        //suma
        int number1 = 7;
        int number2 = 9;
        int resultado = number2 + number1;
        System.out.println("Resultado: " + resultado);
        //resta

        resultado = number2 - number1;
        System.out.println("resultado: " + resultado);
        //multiplicación

        resultado = number2 * number1;
        System.out.println("resultado: " + resultado);

        //division
        resultado = number2 / number1;
        System.out.println("resultado: " + resultado);

        //Porcentaje
        resultado = number2 % number1;
        System.out.println("resultado: " + resultado);

        //Operadores de relacion, logicos, comparación <, >, <=, >=,==, != (diferente), &&, || (or, una de las dos debe cumplirse, ! (false,negación), booleano= false o true






    }
}
